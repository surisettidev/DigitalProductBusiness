// Optional: run via `npm run ai:daily` if no AI agent picks up the work.
// Uses free APIs to draft today's content. Saves to /marketing/YYYY-MM-DD/.
// Requires Node 18+ (built-in fetch) and GROQ_API_KEY in env / GitHub Secrets.

const fs = require('fs');
const path = require('path');

const today = new Date().toISOString().slice(0, 10);
const outDir = path.join(__dirname, '..', 'marketing', today);
fs.mkdirSync(outDir, { recursive: true });

const briefPath = path.join(__dirname, '..', 'config', 'BRIEF.md');
const voicePath = path.join(__dirname, '..', 'config', 'BRAND-VOICE.md');
const brief = fs.readFileSync(briefPath, 'utf8');
const voice = fs.readFileSync(voicePath, 'utf8');

async function callGroq(prompt) {
  const key = process.env.GROQ_API_KEY;
  if (!key) throw new Error('GROQ_API_KEY not set');
  const res = await fetch('https://api.groq.com/openai/v1/chat/completions', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', Authorization: `Bearer ${key}` },
    body: JSON.stringify({
      model: 'llama-3.3-70b-versatile',
      messages: [
        { role: 'system', content: `You write for the Freelancer OS brand.\n\nBRIEF:\n${brief}\n\nVOICE:\n${voice}` },
        { role: 'user', content: prompt }
      ],
      temperature: 0.7
    })
  });
  const json = await res.json();
  return json.choices?.[0]?.message?.content || '';
}

(async () => {
  const tasks = [
    { file: 'twitter.md', prompt: 'Write 3 X/Twitter posts (≤280 chars each) about a common freelancer pricing mistake. Separate posts with "---".' },
    { file: 'reddit.md',  prompt: 'Write one helpful Reddit comment draft for r/IndianFreelancer about freelancer pricing. ≤350 words. Useful first, no promotion.' },
    { file: 'linkedin.md', prompt: 'Write one LinkedIn post (150–250 words) about AI tools that actually save freelancers time. Story or insight format. One soft CTA at the end.' },
    { file: 'email.md',   prompt: 'Write a nurture email (≤200 words) for new subscribers who downloaded the free pricing calculator. Subject line + body. No hard sell.' }
  ];

  for (const t of tasks) {
    console.log(`Generating ${t.file}...`);
    const text = await callGroq(t.prompt);
    fs.writeFileSync(path.join(outDir, t.file), text + '\n');
  }

  // Append to daily log
  const logPath = path.join(__dirname, '..', 'daily-operations', 'daily-log.md');
  const entry = `\n## ${today}\n- Content generated by content-generator.js (Groq fallback)\n- Outputs saved to /marketing/${today}/\n`;
  fs.appendFileSync(logPath, entry);
  console.log('Done.');
})();
